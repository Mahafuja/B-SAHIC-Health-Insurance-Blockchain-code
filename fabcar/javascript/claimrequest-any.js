'use strict';

const { Gateway, Wallets } = require('fabric-network');
const fs = require('fs');
const path = require('path');
const prompt = require('prompt-sync')();
async function main() {
    try {
        // load the network configuration
        const ccpPath = path.resolve(__dirname, '..', '..', 'test-network', 'organizations', 'peerOrganizations', 'org1.example.com', 'connection-org1.json');
        let ccp = JSON.parse(fs.readFileSync(ccpPath, 'utf8'));
        //console.log(cpp);
        // Create a new file system based wallet for managing identities.
        //const walletPath = path.join(process.cwd(), 'wallet');
        const walletPath = '/home/whoami/hyperledger_serverside/fabcar/javascript/wallet';
        const wallet = await Wallets.newFileSystemWallet(walletPath);
        //console.log(`Wallet path: ${walletPath}`);

        // Check to see if we've already enrolled the user.
        const identity = await wallet.get('appUser');
        if (!identity) {
            console.log('An identity for the user "appUser" does not exist in the wallet');
            console.log('Run the registerUser.js application before retrying');
            return;
        }

        // Create a new gateway for connecting to our peer node.
        const gateway = new Gateway();
        await gateway.connect(ccp, { wallet, identity: 'appUser', discovery: { enabled: true, asLocalhost: true } });

        // Get the network (channel) our contract is deployed to.
        const network = await gateway.getNetwork('mychannel');

        // Get the contract from the network.
        const contract = network.getContract('fabcar');

        // Submit the specified transaction.
        // createCar transaction - requires 5 argument, ex: ('createCar', 'CAR12', 'Honda', 'Accord', 'Black', 'Tom')
        // changeCarOwner transaction - requires 2 args , ex: ('changeCarOwner', 'CAR12', 'Dave')
        var result = await contract.submitTransaction('claimrequest', name, claimfor, claimid);
        //result = result.toString();
        //result = JSON.parse(result);
        //console.log(result.lifetimesupport);
        //await contract.submitTransaction('cashless', name, treatmentapplyingfor);
        //console.log('Transaction has been submitted');
        console.log(result.toString());
        // Disconnect from the gateway.
        await gateway.disconnect();

    } catch (error) {
        console.error(`Failed to submit transaction: ${error}`);
        process.exit(1);
    }
}
var arrge = process.argv;
const name = parseInt(arrge[2]);
const claimfor = parseInt(arrge[3]);
const claimid = (arrge[4]);
main();
